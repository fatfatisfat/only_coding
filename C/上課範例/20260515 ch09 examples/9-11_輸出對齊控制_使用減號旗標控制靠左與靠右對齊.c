// 圖 9.11: fig09_11.c
// 數值的靠右對齊與靠左對齊
#include <stdio.h>

int main(void)
{ 
   // 印出比例尺，方便觀察欄位寬度
   puts("1234567890123456789012345678901234567890\n");
   printf("%10s%10d%10c%10f\n\n", "hello", 7, 'a', 1.23);   // 預設情況：欄位寬度 10 且「靠右對齊」
   printf("%-10s%-10d%-10c%-10f\n", "hello", 7, 'a', 1.23); // 使用 - 旗標：欄位寬度 10 且「靠左對齊」
}