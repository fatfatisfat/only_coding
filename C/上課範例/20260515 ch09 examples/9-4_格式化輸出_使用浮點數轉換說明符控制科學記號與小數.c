// 使用浮點數格式指定字
#include <stdio.h>

int main(void)
{ 
   printf("%e\n", 1234567.89); // 以小寫 e 的科學記號顯示
   printf("%e\n", +1234567.89); // 預設情況下正號不會被印出
   printf("%e\n", -1234567.89); // 負號會被印出
   printf("%E\n", 1234567.89); // 以大寫 E 的科學記號顯示
   printf("%f\n", 1234567.89); // 標準小數記數法，預設輸出小數點後六位
   printf("%g\n", 1234567.89); // 自動選擇 %e 或 %f 的精簡形式（此處會以小寫 e 的科學記號顯示）
   printf("%G\n", 1234567.89); // 自動選擇 %E 或 %f 的精簡形式（此處會以大寫 E 的科學記號顯示）
}